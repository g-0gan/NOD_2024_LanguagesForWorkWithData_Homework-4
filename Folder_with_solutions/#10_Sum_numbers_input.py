def sum_numbers():
    """
    Запрашивает у пользователя ввод чисел и вычисляет их сумму.

    Пользователь может вводить как целые, так и вещественные числа.
    Программа завершает свою работу, когда пользователь вводит пустую строку.
    Если ввод не является корректным числом, выводится предупреждение.
    """
    total_sum = 0.0  # Используем тип float для обработки целых и вещественных чисел

    while True:
        user_input = input("Введите число (или просто нажмите Enter для выхода): ").strip()

        if not user_input:
            break  # Выход из программы при пустом вводе

        try:
            number = float(user_input)  # Пробуем преобразовать ввод в число с плавающей запятой
            total_sum += number
            print(f"Текущая сумма: {total_sum}")
        except ValueError:
            print("Предупреждение: Пожалуйста, введите корректное числовое значение.")


if __name__ == "__main__":
    sum_numbers()
